using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ISD_Course_task_5_static
{
    static class Calculator
    {
        public static double Add(double param1, double param2)
        {
            return param1 + param2;
        }
        public static double Minus(double param1, double param2)
        {
            return param1 - param2;
        }
        public static double Devide(double param1, double param2)
        {
            return param1 / param2;
        }
        public static double Multiply(double param1, double param2)
        {
            return param1 * param2;
        }
    }
}
